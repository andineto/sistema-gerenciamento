    private void btnAdicionarActionPerformed(ActionEvent evt) {                                             
        // TODO add your handling code here:
        int linhaSelecionada = produtosParaAdd.getSelectedRow();
        int idSelecionado = (int) produtosParaAdd.getValueAt(linhaSelecionada,0);
        Produto produtoSelecionado;
        for (Produto produto : listaProdutos){
            if (produto.getId() == idSelecionado){
                produtoSelecionado = produto;
                addAoCarrinho(produtoSelecionado);
                break;
            }
        }

    }


    private void btnFecharCarrinhoActionPerformed(ActionEvent evt){//GEN-FIRST:event_btnFecharCarrinhoActionPerformed
        // TODO add your handling code here:
        Venda venda = carrinho.fecharCarrinho();
        int confirmacao = JOptionPane.showConfirmDialog(
                this,
                "O valor total da venda é de: R$ " + String.format("%.2f", venda.getValorTotal()) + "\nDeseja confirmar a venda?",
                "Confirmar Nova Venda",
                JOptionPane.YES_NO_OPTION);
        if (confirmacao == JOptionPane.YES_OPTION) {
            // Registrar a venda e os itens no banco de dados
            venda.registrarVenda();
                JOptionPane.showMessageDialog(this, "Venda registrada com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao registrar a venda!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        limparCarrinho();
    }//GEN-LAST:event_btnFecharCarrinhoActionPerformed

