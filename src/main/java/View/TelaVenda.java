    private void setProdutosNaLista(){
        ProdutosDAO pd = new ProdutosDAO();
        listaProdutos = pd.consultarProdutos();
        dfListaProdutos = new DefaultTableModel(new String[]{"ID", "Nome", "Preço"}, 0);
        Object[] row;
        for(Produto produto : listaProdutos){
            row = new Object[]{produto.getId(), produto.getNome(), produto.getPreco()};
            dfListaProdutos.addRow(row);
        }
        produtosParaAdd.setModel(dfListaProdutos);
    }

    private void addAoCarrinho(Produto produto) {
        try {
            int quantidade = Integer.parseInt(txtQtd.getText());
            if (quantidade <= 0) {
                JOptionPane.showMessageDialog(this, "Quantidade inválida!");
                return;
            }
            carrinho.add(produto, quantidade);
            Object[] row = {produto.getId(), produto.getNome(), quantidade, quantidade*produto.getPreco()};
            dfCarrinho.addRow(row);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Insira um valor numérico para a quantidade!");
        }
    }

    private void limparCarrinho(){
        dfCarrinho.setRowCount(0);
        carrinho.getItens().clear();
    }
